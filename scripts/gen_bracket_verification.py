def mat_entries(M):
    """Get nonzero entries of a 16x16 Fraction matrix"""
    entries = []
    for r in range(16):
        for c in range(16):
            if M[r][c] != 0:
                entries.append((r, c, M[r][c]))
    return entries

def frac_to_lean(f):
    """Convert Fraction to Lean Q literal"""
    if f == Fraction(0):
        return "0"
    elif f == Fraction(1, 2):
        return "1/2"
    elif f == Fraction(-1, 2):
        return "-1/2"
    elif f == Fraction(1, 4):
        return "1/4"
    elif f == Fraction(-1, 4):
        return "-1/4"
    elif f == Fraction(1):
        return "1"
    elif f == Fraction(-1):
        return "-1"
    else:
        return f"{f.numerator}/{f.denominator}"


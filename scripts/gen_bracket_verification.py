def to_fraction(x):
    """Convert float to exact fraction (should be +-1/2 or 0)"""
    if abs(x) < 1e-10:
        return Fraction(0)
    elif abs(x - 0.5) < 1e-10:
        return Fraction(1, 2)
    elif abs(x + 0.5) < 1e-10:
        return Fraction(-1, 2)
    elif abs(x - 0.25) < 1e-10:
        return Fraction(1, 4)
    elif abs(x + 0.25) < 1e-10:
        return Fraction(-1, 4)
    elif abs(x - 1.0) < 1e-10:
        return Fraction(1)
    elif abs(x + 1.0) < 1e-10:
        return Fraction(-1)
    else:
        raise ValueError(f"Unexpected value: {x}")


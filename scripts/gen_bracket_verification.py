def is_same_parity(i, j):
    """Same parity means both odd or both even"""
    return (i % 2) == (j % 2)

# === Step 2: Compute structure constants ===
# [L_{ij}, L_{kl}] = d_{jk}L_{il} - d_{ik}L_{jl} - d_{jl}L_{ik} + d_{il}L_{jk}
# where d = Kronecker delta (standard so(10))

def bracket_result(a_idx, b_idx):
    """Compute [e_a, e_b] as a dict {c_idx: coefficient}"""
    i, j = pairs[a_idx]
    k, l = pairs[b_idx]

    terms = []
    # d_{jk}*L_{il}
    if j == k:
        terms.append((i, l, 1))
    # -d_{ik}*L_{jl}
    if i == k:
        terms.append((j, l, -1))
    # -d_{jl}*L_{ik}
    if j == l:
        terms.append((i, k, -1))
    # d_{il}*L_{jk}
    if i == l:
        terms.append((j, k, 1))

    result = {}
    for p, q, sign in terms:
        if p == q:
            continue  # L_{pp} = 0
        if p < q:
            c_idx = pair_to_idx[(p, q)]
            result[c_idx] = result.get(c_idx, 0) + sign
        else:  # p > q: L_{pq} = -L_{qp}
            c_idx = pair_to_idx[(q, p)]
            result[c_idx] = result.get(c_idx, 0) - sign

    # Remove zeros
    result = {k: v for k, v in result.items() if v != 0}
    return result

# === Step 3: Build Cl(5,5) gamma matrices (ALL REAL) ===

def build_gamma_matrices_cl55(n):
    """Build n gamma matrices for Cl(5,5) using tensor product construction.

    Uses sigma_x (gamma^2=+I) and isy=[[0,1],[-1,0]] (gamma^2=-I).
    Odd-indexed gammas square to +I, even-indexed to -I.
    ALL matrices are REAL."""
    num_qubits = n // 2

    # Real 2x2 matrices
    I2 = np.eye(2, dtype=float)
    sx = np.array([[0, 1], [1, 0]], dtype=float)        # sx^2 = I
    isy = np.array([[0, 1], [-1, 0]], dtype=float)      # isy^2 = -I
    sz = np.array([[1, 0], [0, -1]], dtype=float)        # sz^2 = I

    gammas = []
    for k in range(num_qubits):
        # gamma_{2k+1} = sz x ... x sz x sx x I x ... x I  (squares to +I)
        mat = np.array([[1]], dtype=float)
        for q in range(num_qubits):
            if q < k:
                mat = np.kron(mat, sz)
            elif q == k:
                mat = np.kron(mat, sx)
            else:
                mat = np.kron(mat, I2)
        gammas.append(mat)

        # gamma_{2k+2} = sz x ... x sz x isy x I x ... x I  (squares to -I)
        mat = np.array([[1]], dtype=float)
        for q in range(num_qubits):
            if q < k:
                mat = np.kron(mat, sz)
            elif q == k:
                mat = np.kron(mat, isy)
            else:
                mat = np.kron(mat, I2)
        gammas.append(mat)

    return gammas

